export function CommitsGrid({ text, className }: CommitsGridProps) {
  const [animationKey, setAnimationKey] = React.useState(0);

  React.useEffect(() => {
    setAnimationKey((prev) => prev + 1);
  }, [text]);

  // Convert text to grid pattern
  const createGrid = () => {
    if (!text) return [[false]]; // Return minimal grid if text is undefined

    const letters = text
      .split("")
      .filter((char) => letterPatterns[char.toUpperCase()]);
    const totalWidth = letters.length * 6 - 1; // 5 width + 1 spacing, minus last spacing
    const height = 7;

    const grid: boolean[][] = Array(height)
      .fill(null)
      .map(() => Array(totalWidth).fill(false));

    letters.forEach((letter, letterIndex) => {
      const pattern = letterPatterns[letter.toUpperCase()];
      const startX = letterIndex * 6;

      pattern.forEach((row, y) => {
        row.forEach((cell, x) => {
          if (startX + x < totalWidth) {
            grid[y][startX + x] = cell === 1;
          }
        });
      });
    });

    return grid;
  };

  const grid = createGrid();

  return (
    <div className={cn("inline-block", className)}>
      <div
        className="grid gap-[2px] p-4"
        style={{ gridTemplateColumns: `repeat(${grid[0]?.length || 1}, 8px)` }}
      >
        {grid.flat().map((isActive, index) => (
          <div
            key={`${animationKey}-${index}`}
            className={cn(
              "w-2 h-2 rounded-[1px] transition-all duration-300",
              isActive
                ? "bg-green-400 animate-flash shadow-sm shadow-green-400/50"
                : "bg-gray-800"
            )}
            style={{
              animationDelay: `${(index % 10) * 50}ms`,
            }}
          />
        ))}
      </div>
    </div>
  );
}
